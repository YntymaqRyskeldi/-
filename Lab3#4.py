'''Үстел ойыны үшін 1-ден N-ге дейінгі сандары бар карталар пайдаланылады.
Бір карта жоғалады. Қалған карталардың сандарын білу арқылы оны табыңыз.
N саны берілген, содан кейін N − 1 қалған карталардың саны
(1-ден N-ге дейінгі әртүрлі сандар). Бағдарлама жоғалған картаның нөмірін
көрсетуі керек.'''
N = int(input("N = "))

sum = 0
for i in range(1, N + 1):
    sum += i

for i in range(N - 1):
    sum -= int(input())

print('\n', sum)
